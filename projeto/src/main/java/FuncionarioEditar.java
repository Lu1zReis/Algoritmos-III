    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        // TODO add your handling code here:
        String nome = editarFuncionarioNome.getText();
        String cpf = editarFuncionarioCpf.getText();
        String cargo = editarFuncionarioCargo.getText();
        String idadeSemFiltro = editarFuncionarioIdade.getText();
        String salarioSemFiltro = editarFuncionarioSalario.getText();
        String idFiltro = editarFuncionarioId.getText();
        
        if (nome.equals("") || cpf.equals("") || cargo.equals("") || idadeSemFiltro.equals("") || salarioSemFiltro.equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos");
        } 
        else if (idFiltro.equals("")) {
            JOptionPane.showMessageDialog(this, "Adicione um id válido.");
            return; // Saia do método se o funcionário não for encontrado
        }
        else {
            try {
                FuncionarioView fView = new FuncionarioView();

                // pegar de view o id

                int idade = Integer.parseInt(idadeSemFiltro);
                float salario = Float.parseFloat(salarioSemFiltro);
                
                int id = Integer.parseInt(idFiltro);

                Funcionario f = new Funcionario();
                FuncionarioDAO fDAO = new FuncionarioDAO();

                f.setId(id);
                f.setNome(nome);
                f.setCargo(cargo);
                f.setCpf(cpf);
                f.setIdade(idade);
                f.setSalario(salario);

                if (fDAO.atualizarFuncionario(f)) JOptionPane.showMessageDialog(this, "Funcionário editado!");
                else JOptionPane.showMessageDialog(this, "Não foi possível editar funcionário!");

                this.dispose();

                fView.setVisible(true);

            } catch (SQLException ex) {
                Logger.getLogger(FuncionarioCadastrar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btn_editarActionPerformed

